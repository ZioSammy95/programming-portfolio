package eserciziojava;

public class Switch {
	public static void main(String[]args) {
		String nome = "";
		
		switch(nome) {
		case "Luca":
			System.out.println("ciao Luca");
			break;
		case "Marco":
			System.out.println("ciao Marco");
		    break;
		case "Samuele":
			System.out.println("ciao Samuele");
			break;
		case "Antonio":
			System.out.println("ciao Antonio");
			break;
		case "Edoardo":
			System.out.println("ciao Edoardo");
			break;
			default:
				System.out.println("ciao chiunque tu sia");
						
		}

}

}