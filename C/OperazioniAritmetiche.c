#include <stdio.h>

int main() {
    int a = 10, b = 3;
    double x = 10.0, y = 3.0;
    printf("Somma: %d\n", a + b);
    printf("Differenza: %d\n", a - b);
    printf("Prodotto: %d\n", a * b);
    printf("Divisione intera: %d\n", a / b);  // Risultato intero
    printf("Divisione: %.2f\n", x / y); // Stampa 3.33
    printf("Resto: %d\n", a % b); // Modulo
    return 0;
    



}
